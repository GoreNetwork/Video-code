import ipaddress

def make_subnet(text):
    return ipaddress.ip_network(text, strict = False)

def make_ip_address(text):
    return ipaddress.ip_address(text)

def split_subnets(text,new_cider_number):
    return list(ipaddress.ip_network(text).subnets(new_prefix = new_cider_number))



